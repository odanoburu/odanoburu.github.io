◊book[#:author "Naomi Klein" #:published "2014" #:title
"This changes everything" #:subtitle "Capitalism versus The Climate"
#:status "read"]{

◊synopsis{

Forget everything you think you know about global warming. The really
inconvenient truth is that it’s not about carbon—it’s about
capitalism. The convenient truth is that we can seize this existential
crisis to transform our failed economic system and build something
radically better.

In her most provocative book yet, Naomi Klein, author of the global
bestsellers The Shock Doctrine and No Logo, tackles the most profound
threat humanity has ever faced: the war our economic model is waging
against life on earth.

Klein exposes the myths that are clouding the climate debate. We have
been told the market will save us, when in fact the addiction to
profit and growth is digging us in deeper every day. We have been told
it’s impossible to get off fossil fuels when in fact we know exactly
how to do it—it just requires breaking every rule in the “free-market”
playbook: reining in corporate power, rebuilding local economies, and
reclaiming our democracies.

We have also been told that humanity is too greedy and selfish to rise
to this challenge. In fact, all around the world, the fight for the
next economy and against reckless extraction is already succeeding in
ways both surprising and inspiring.

Climate change, Klein argues, is a civilizational wake-up call, a
powerful message delivered in the language of fires, floods, storms,
and droughts. Confronting it is no longer about changing the light
bulbs. It’s about changing the world—before the world changes so
drastically that no one is safe.   Either we leap—or we sink.

Once a decade, Naomi Klein writes a book that redefines its era. No
Logo did so for globalization. The Shock Doctrine changed the way we
think about austerity. This Changes Everything is about to upend the
debate about the stormy era already upon us.

}

◊opinion{

I agree with most of the diagnosis of the problem and with the general
idea towards a solution. I'm not so sure about the specificities of
the solutions—free trade still seems like a good thing to me, just
like free migration.

}

}

◊book[#:author "Jonathan Haidt" #:published "2012" #:title
"The righteous mind" #:subtitle "why good people are divided by politics and religion"
#:status "read"]{

◊synopsis{

See the book's webpage.

}

◊opinion{

I believe political discussion should be about convincing people of
our ideas (whereas a lot of people nowadays seem to think that it's
meant to inflate their standing among the people who already agree
with them). Most people have good intentions, even if their political
beliefs might be contrary to these intentions (or so we might
think). We should strive to convince them, not to alienate them. (It's
funny that I use a “us versus them” discourse to try to undermine this
mentality.)

This book tries to explain how humans develop their moral beliefs,
which has implications for their political and religious
views. Understanding why someone thinks a certain way helps us reduce
the divisiveness of our current political climate, helping us approach
problems together despite our clashing beliefs.

This is one of those books we all should read.


}

}

